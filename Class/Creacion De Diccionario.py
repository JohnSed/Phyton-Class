def sumar(a, b):#Definir Una Funcion Don a y b son parametros posicionales, Los parámetros posicionales se definen en el orden en que se esperan los valores al llamar a la función o procedimiento.
    resultado = a + b
    return resultado
#Al invocar la función, debemos proporcionar los valores en el mismo orden:
resultado = sumar(6,3)
print(resultado)



